#include "../headers/libft.h"

ssize_t	ft_putchar_fd(char c, int fd)
{
	ssize_t	written_chars;

	written_chars = 0;
	if (fd < 0)
		return (-1);
	written_chars = write(fd, &c, 1);
	return (written_chars);
}
